package com.virtual.portable.server.model;

public class TouchCoordinates {

    Integer x_coor;
    Integer y_coor;

    public Integer getX_coor() {
        return x_coor;
    }

    public void setX_coor(Integer x_coor) {
        this.x_coor = x_coor;
    }

    public Integer getY_coor() {
        return y_coor;
    }

    public void setY_coor(Integer y_coor) {
        this.y_coor = y_coor;
    }
}
